void OLED_PrintText(OLED_t* oled, uint8_t x, uint8_t y, const char* str, uint8_t size) {
    while (*str) {
        if (*str == '\n') {
            x = 0;
            y += size;
            if (y >= oled->height_) y = 0;
            OLED_SetCursor(oled, x, y);
        } else if (*str >= 0x20 && *str <= 0x7E) {
            uint8_t charIndex = *str - 0x20;
            if (size == 8) {
                // 6x8字体
                OLED_SetCursor(oled, x, y);
                for (uint8_t i = 0; i < 6; i++) {
                    OLED_WriteData(oled, font6x8[charIndex][i]);
                }
                x += 6;
            } else if (size == 16) {
                // 8x16字体
                for (uint8_t row = 0; row < 2; row++) {
                    OLED_SetCursor(oled, x, y + row);
                    for (uint8_t i = 0; i < 8; i++) {
                        OLED_WriteData(oled, font8x16[charIndex][row*8 + i]);
                    }
                }
                x += 8;
            }
            
            if (x >= 128) {
                x = 0;
                y += size;
                if (y >= oled->height_) y = 0;
            }
        }
        str++;
    }
}

void OLED_PrintImage(OLED_t* oled, uint8_t x, uint8_t y, uint8_t width, uint8_t height, uint8_t* image) {
    for (uint8_t i = 0; i < height; i++) {
        OLED_SetCursor(oled, x, y + i);
        for (uint8_t j = 0; j < width; j++) {
            OLED_WriteData(oled, image[i * width + j]);
        }
    }
}

uint8_t cmpStrings(const char* str1, const char* str2) {
    while (*str1 && *str2) {    // 当两个字符串都没结束时
        if (*str1 != *str2) {
            return 0;           // 如果字符不同，返回 false
        }
        str1++;
        str2++;
    }
    return (*str1 == *str2);    // 如果两者都到达字符串末尾，则相等
}

void OLED_PrintVar(OLED_t* oled, uint8_t x, uint8_t y, float num, const char* type, uint8_t length, bool zeroPad) {
    uint8_t i, j;
    char buffer[16];
    
    // 初始化缓冲区
    if (zeroPad) {
        memset(buffer, '0', length);
    } else {
        memset(buffer, ' ', length);
    }
    buffer[length] = '\0';
    
    if (cmpStrings(type, "int")) {
        // 整数显示，右对齐
        int32_t value = (int32_t)num;
        char temp[16];
        sprintf(temp, "%d", value);
        uint8_t len = strlen(temp);
        if (len > length) len = length;
        if (zeroPad && value < 0) {
            // 处理负数补零
            memcpy(buffer + (length - len + 1), temp + 1, len - 1);
            buffer[length - len] = '-';
        } else {
            memcpy(buffer + (length - len), temp, len);
        }
    } 
    else if (cmpStrings(type, "uint")) {
        // 无符号整数显示，右对齐
        uint32_t value = (uint32_t)num;
        char temp[16];
        sprintf(temp, "%u", value);
        uint8_t len = strlen(temp);
        if (len > length) len = length;
        memcpy(buffer + (length - len), temp, len);
    }
    else if (cmpStrings(type, "float")) {
        // 浮点数显示，固定小数位数
        snprintf(buffer, sizeof(buffer), "%.2f", num);
        // 确保长度不超过指定值
        if (strlen(buffer) > length) {
            buffer[length] = '\0';
        }
    } 
    else if (cmpStrings(type, "hex")) {
        // 十六进制显示，右对齐
        uint16_t value = (uint16_t)num;
        char temp[16];
        sprintf(temp, "%X", value);
        uint8_t len = strlen(temp);
        if (len > length) len = length;
        memcpy(buffer + (length - len), temp, len);
    } 
    else {
        sprintf(buffer, "ERR");
    }

    // 显示结果
    for (j = 0; buffer[j] != '\0'; j++) {
        OLED_SetCursor(oled, x, y);
        for (i = 0; i < 6; i++) {
            OLED_WriteData(oled, font6x8[buffer[j] - ' '][i]);
        }
        x += 6;
        if (x > 127) {
            x = 0;
            y++;
        }
    }
}

void OLED_PrintHLine(OLED_t* oled, uint8_t x, uint8_t y, uint8_t width) {
    OLED_SetCursor(oled, x, y);
    for (uint8_t i = 0; i < width; i++) {
        OLED_WriteData(oled, 0xFF);
    }
}

void OLED_LowBrightness(OLED_t* oled, bool enable) {
    OLED_WriteCommand(oled, 0x81);
    OLED_WriteCommand(oled, enable ? 0x10 : 0xCF);
}

void OLED_Power(OLED_t* oled, bool state) {
    OLED_WriteCommand(oled, state ? 0xAF : 0xAE);
}
