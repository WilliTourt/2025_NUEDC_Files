uint8_t displayBuffer[4][128] = {0}; // Only use lower half (pages 4-7)

static void OLED_WriteCommand(OLED_t* oled, uint8_t command) {
    uint8_t temp[2];
    temp[0] = 0x00;     // Command mode
    temp[1] = command;

    DL_I2C_fillControllerTXFIFO(oled->I2C_PORT_, temp, 2);
    while (!(DL_I2C_getControllerStatus(oled->I2C_PORT_) & DL_I2C_CONTROLLER_STATUS_IDLE));
    DL_I2C_startControllerTransfer(oled->I2C_PORT_, OLED_ADDR, DL_I2C_CONTROLLER_DIRECTION_TX, 2);
    while (DL_I2C_getControllerStatus(oled->I2C_PORT_) & DL_I2C_CONTROLLER_STATUS_BUSY_BUS);
    while (!(DL_I2C_getControllerStatus(oled->I2C_PORT_) & DL_I2C_CONTROLLER_STATUS_IDLE));
    DL_I2C_flushControllerTXFIFO(oled->I2C_PORT_);
}

static void OLED_WriteData(OLED_t* oled, uint8_t data) {
    uint8_t temp[2];
    temp[0] = 0x40;     // Data mode
    temp[1] = data;

    DL_I2C_fillControllerTXFIFO(oled->I2C_PORT_, temp, 2);
    while (!(DL_I2C_getControllerStatus(oled->I2C_PORT_) & DL_I2C_CONTROLLER_STATUS_IDLE));
    DL_I2C_startControllerTransfer(oled->I2C_PORT_, OLED_ADDR, DL_I2C_CONTROLLER_DIRECTION_TX, 2);
    while (DL_I2C_getControllerStatus(oled->I2C_PORT_) & DL_I2C_CONTROLLER_STATUS_BUSY_BUS);
    while (!(DL_I2C_getControllerStatus(oled->I2C_PORT_) & DL_I2C_CONTROLLER_STATUS_IDLE));
    DL_I2C_flushControllerTXFIFO(oled->I2C_PORT_);
}

void OLED_Clear(OLED_t* oled) {
    uint8_t i, j;
    uint8_t lines = 8;

    if (oled->height_ == 32) {
        lines = 4;
    }

    for (i = 0; i < lines; i++) {
        OLED_SetCursor(oled, 0, i);
        for (j = 0; j < 128; j++) {
            OLED_WriteData(oled, 0x00);
        }
    }
    OLED_SetCursor(oled, 0, 0);
}

void OLED_Init(OLED_t* oled, uint8_t height, I2C_Regs* I2C_PORT) {
    oled->height_ = height;
    oled->I2C_PORT_ = I2C_PORT;
}

bool OLED_Begin(OLED_t* oled) {
    OLED_WriteCommand(oled, 0xAE);
    OLED_WriteCommand(oled, 0xD5); OLED_WriteCommand(oled, 0x80);
    OLED_WriteCommand(oled, 0xA8); OLED_WriteCommand(oled, oled->height_ == 32 ? 0x1F : 0x3F);
    OLED_WriteCommand(oled, 0xD3); OLED_WriteCommand(oled, 0x00);
    OLED_WriteCommand(oled, 0x40); OLED_WriteCommand(oled, 0x8D); OLED_WriteCommand(oled, 0x14);
    OLED_WriteCommand(oled, 0x20); OLED_WriteCommand(oled, 0x00);
    OLED_WriteCommand(oled, 0xA1); OLED_WriteCommand(oled, 0xC8);
    OLED_WriteCommand(oled, 0xDA); OLED_WriteCommand(oled, oled->height_ == 32 ? 0x02 : 0x12);
    OLED_WriteCommand(oled, 0x81); OLED_WriteCommand(oled, 0xCF);
    OLED_WriteCommand(oled, 0xD9); OLED_WriteCommand(oled, 0xF1);
    OLED_WriteCommand(oled, 0xDB); OLED_WriteCommand(oled, 0x40);
    OLED_WriteCommand(oled, 0xA4); OLED_WriteCommand(oled, 0xA6); OLED_WriteCommand(oled, 0xAF);

    OLED_Clear(oled);
    return true;
}

void OLED_SetCursor(OLED_t* oled, uint8_t x, uint8_t y) {
    uint8_t lines = oled->height_ == 32 ? 3 : 7;
    if (y > lines) y = lines;
    if (x > 127) x = 127;
    
    OLED_WriteCommand(oled, 0xB0 + y); // Set page address
    OLED_WriteCommand(oled, 0x00 | (x & 0x0F)); // Set lower column address
    OLED_WriteCommand(oled, 0x10 | ((x & 0xF0) >> 4)); // Set higher column address
}

void OLED_SetPixel(OLED_t* oled, uint8_t x, uint8_t y, bool state) {
    if (x >= 128 || y < 32 || y >= 64) return;
    uint8_t page = (y - 32) / 8; // 仅用4页
    uint8_t bit = y % 8;

    if (state) {
        displayBuffer[page][x] |= (1 << bit);
    } else {
        displayBuffer[page][x] &= ~(1 << bit);
    }
}

